		<h2>Inlämningsuppgift 2</h2>
		<h3><span class="a" id="m1"></span>1. Inledning</h3>
		<p>I den andra inlämningsuppgiften ska ni bygga en webbsida med tabeller, bilder. Innehållet på webbsidan ska visa en tabell med information om musik artister från Sverige, Australien och USA (tillsammans med en bild på den valda artisten). Ni får fria händer på webbplatsens utformning - så länge ni följer bild exemplet och kravlistan som finns längre ner i uppgiften. Den innehåller bl.a. krav på vilka element ni måste använda, vilka bild format som gäller och att sidan ska validera enligt HTML 5-standarden.</p>
        <h3><span class="a" id="m2"></span>2. Syfte</h3>
		<p>Syftet med denna inlämningsuppgift är att ni ska få en bra grund om hur man gör tabeller i HTML, infogar bilder i tabeller, infogar länkar i tabeller, känna till hur bild format hanteras och ha kunskap om vilka taggar som ska användas. Ni ska även ha kunskap om HTML 5-standarden och följa denna, så att er webbplats valideras enligt denna standard.</p>
        
		<h3><span class="a" id="m3"></span>3. Uppgiften</h3>
        <h4><span class="a" id="m31"></span>Den egna tabellen med musik artisterna</h4>
		<p>Uppgiften är alltså att skapa en HTML sida som presenterar musik artister från tre olika länder (Australien, Sverige och USA) i form av en tabell. Webbsidan ska innehålla följande: en omslags bild, information om bandets namn, medlemmar, deras senaste album, från vilket land de kommer från och källa på var ni har tagit informationen. Det skall vara totalt 10 musik artister med minst 2 från varje land. All denna information skall vara i tabellen. De element som ni måste ha med på sidan <i>minst en gång</i> är:</p>
        <pre>&lt;th&gt;, &lt;tr&gt;, &lt;td&gt;, &lt;table&gt;, &lt;img&gt;, &lt;a&gt; (överkurs/frivilligt: &lt;tbody&gt;, &lt;thead&gt;, &lt;caption&gt;)</pre> 
        <p style="margin-top:-10px; text-align:center;"><i>Figur. 1</i></p>
		<p>Ni skall även använda er av tre olika filformat för bilderna i eran tabell. Formatet skall finnas minst en gång är följande:</p>
		<pre> .bmp, .jpg, och .png</pre>
		<p style="margin-top:-10px; text-align:center;"><i>Figur. 2</i></p>
		
		<p>Innehållet i tabellen skall vara kortfattande för varje del men heltäckande. Länk till var ni har hämtat informationen skall även finnas med.</p>
       
        <p>Sidan ska sedan valideras, genom denna sida: <a href="http://validator.w3.org/">http://validator.w3.org/</a>. Inga fel ska då visas (varningar är OK). Er kod ska vara bra <a href="http://sv.wikipedia.org/wiki/Indentering">indenterad</a>.</p>
