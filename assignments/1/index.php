        </p>
        <p style="margin-top:-10px; text-align:center;"><i>Bild. 1</i></p>
        <p><i>Det ska även från denna webbsida finnas en länk som leder till er webbsida med frågor & svar om HTML (del 2 av inlämningsuppgiften).</i></p>
        
        <h4>3.2. Del 2: Frågor </h4>
        <p>Dessa frågorna ska du besvara på en webbsida:</p>
        <ol>
            <li>Förklara kort vad ett <i>element</i>, <i>attribut</i> samt <i>värde</i> är?</li>
            <li>Förklara vad HTTP är för något och förklara vad ett <i>kommunikationsprotokoll</i> är.</li>
            <li>Vilka är <i>W3C</i>, och vilken är deras huvudsakliga uppgift?</li>
            <li>Vad betyder <i>WWW</i> och vad är det för något?</li>
            <li>I bilden nedan visas en typisk internetadress:<br><img src="/assignments/1/url.png" alt="Bild på webbadress"><br>4.1. Vad kallas denna internetadress på fackspråk (en förkortning).<br>4.2. Vad betyder denna förkortning?<br>4.3. Vad kallas <i>a</i>, <i>b</i> och <i>c?</i></li>
            <li>Vad använder man kommunikationsprotokollet <i>FTP</i> till?</li>
            <li>Vilken modell bygger <i>HTTP</i> på? Modellen beskriver förhållandet mellan en dator som svarar och en dator som ställer förfrågan om att få se webbsidorna?</li>
            <li>Vad betyder <i>HTML</i> och vad är det ett exempel på för datorspråk?</li>
            <li>Varför använder man så kallad <i>indentering</i> (rad-indrag) när man skriver HTML-kod?</li>
            <li>Blir det någon större skillnad på själva sidan när du  <i>indenterar</i> HTML-koden?</li>
            <li>Varför tror du att man lär sig behärska HTML-kod från grunden till skillnad från att generera HTML-kod i så kallade WYSIWYG-programvaror eller HTML-generatorer?</li>
            <li>Vilka två delar kan man dela in ett HTML-dokument i? Vilken sorts information ska finnas i de olika delarna?</li>
            <li>Varför har man s.k. kommentarer i sin HTML-kod?</li>
        </ol>
        <p>Svaren ska vara kortfattade, men heltäckande och presenteras på en egen HTML-sida (som ni skriver). Källor till svaren kan antingen vara kurslitteratur eller internetkällor.</p>
        <p>Även denna webbsida ska sedan valideras, genom sidan: <a href="http://validator.w3.org/">http://validator.w3.org/</a>. Inga fel ska då visas (varningar är OK). Er kod ska vara bra <a href="http://sv.wikipedia.org/wiki/Indentering">indenterad</a>, tips för att lyckas med detta är <a href="http://www.dirtymarkup.com/">denna webbplats</a>.</p>        
        
        <p>Det ska även från denna webbsida finnas en länk som leder till er webbsida med där ni presenterar er själva (del 1 av inlämningsuppgiften).</p>
        
        <h3>4. Krav</h3>
        <h4>4.1. Generella krav</h4>
        <ul>
            <li>All HTML-kod ska validera enligt W3Cs HTML 5-standard (validera dina sidor <a href="http://validator.w3.org" target="_blank">här</a>)</li>
            <li>All HTML-kod ska vara bra indenterad</li>
            <li>Ni ska använda rätt HTML-element för de olika typerna av information ni har på er sidan. T.ex. ska er största rubrik vara &lt;h1&gt;-element, era paragrafer ska vara &lt;p&gt;-element och era punkter i era listor ska vara &lt;li&gt;-element.</li>
        </ul>
        <h4>4.2. Specifika krav</h4>
        <h5>4.2.1. Del 1. Egen webbsida</h5>
        <ul>
            <li>Alla element som finns visade i <i>figur 1</i> ska finnas med på er webbsida minst en gång.</li>
            <li>Informationen på er webbsida ska vara utformad så att elementeten i <i>Figur 1</i> finns representerade på ett <i>korrekt sätt</i>. Alltså för att använda &lt;li&gt;-element behöver ni information på er webbsida som representerar en lista.</li>
            <li>Det ska finnas en länk från denna webbsida till er sida med frågor och svar om HTML.</li>
        </ul>
        <h5>4.2.2. Del 2. Frågor</h5>
        <ul>
            <li>Alla frågor ska besvaras med korrekta svar</li>
            <li>Det ska finnas en länk från denna webbsida till er sida där ni presenterar er själva.</li>
        </ul>
        
        <p>Även kvalitén på er kod kommer även att bedömas.</p>
                
