		<h2>Inlämningsuppgift 1</h2>
		<h3>1. Inledning</h3>
		<p>I den första inlämningsuppgiften ska vi (1) bygga vår allra första webbsida. Innehållet på webbsidan ska handla om er själva (tillsammans med minst en bild). Ni får väldigt fria händer på webbplatsens utformning - så länge ni följer kravlistan som finns längre ner i uppgiften. Den innehåller bl.a. krav på vilka element ni måste använda (minst en gång), samt att sidan ska validera enligt HTML 5-standarden.</p>
        <p>Vi ska även (2) svara på några grundläggande frågor om webbutveckling. Svaren på dessa frågor ska ni presentera i form av en webbsida skriven i HTML. Ni hittar svaren på dessa frågor antingen i kurslitteratur eller genom informationssökning på internet.</p>
       <p><b>Ni ska alltså skapa två stycken webbsidor (två HTML-dokument).</b></p>
       
		<h3>2. Syfte</h3>
		<p>Syftet med denna inlämningsuppgift är att ni ska få en bra grund, samt förståelse om hur märkspråket HTML fungerar och att ni efter slutförd uppgift har kunskap om vilka element som ska användas i vilka situationer (beroende på vilket innehåll som ska presenteras). Ni ska även ha kunskap om W3Cs HTML 5-standard och följa denna när ni skapar webbsidor, så att er webbplats validerar enligt HTML 5.</p>
        
		<h3>3. Uppgiften</h3>
        <h4>3.1. Del 1: Den egna webbsidan</h4>
		<p>Uppgiften är alltså att skapa en webbsida som persenterar er själva. Webbsidan ska innehålla en kort bakgrund om vem ni är, minst en bild på er samt några underrubriker, länkar, listor enligt eget val. De element som ni måste ha med på sidan <i>minst en gång</i> är:</p>
        <pre>&lt;h1&gt;, &lt;h2&gt;, &lt;h3&gt;, &lt;p&gt;, &lt;img&gt;, &lt;ul&gt;,  &lt;ol&gt;, &lt;li&gt;, &lt;a&gt;, &lt;i&gt;, &lt;b&gt;</pre>
        <p style="margin-top:-10px; text-align:center;"><i>Figur. 1</i></p>
        <p>Sidan ska sedan valideras, genom denna sida: <a href="http://validator.w3.org/">http://validator.w3.org/</a>. Inga fel ska då visas (varningar är OK). Er kod ska vara bra <a href="http://sv.wikipedia.org/wiki/Indentering">indenterad</a>, tips för att lyckas med detta är <a href="http://www.dirtymarkup.com/">denna webbplats</a>.</p>
        <p>Exempel på hur sidan skulle kunna se ut när den är klar:</p>
        <p>
