<h2>Modul 5 - Layout med CSS</h2>
<h3>1. Kom igång med layoutbyggande</h3>
<p>Vi har nu lärt oss grundläggande HTML för att strukturera upp vår information på de webbplatser vi hittills skapat, men också tittat på CSS för att formge denna information. Vi ska nu blicka framåt och fokusera mer på hur man kan skapa layouter på webbplatserna för bättre/tydligare/snyggare presentation av information. Här tänker vi mest på att våra sidor bör ha ett sidhuvud, en meny, en plats för sidans innehåll, sidfot, etc.</p>

<p>Vi ska också lära oss hur vi positionerar ut element på webbplatsen, t.ex. ska de ligga till höger eller vänster på sidan? Detta genom att titta på CSS och HTML för layout. Nya element som kan komma att användas är bl.a. (<code>&lt;header&gt;, &lt;footer&gt;, &lt;nav&gt;, &lt;aside&gt;, &lt;section&gt;, &lt;article&gt;</code>). Nya CSS egenskapar som vi kommer behöva titta på är t.ex. <code>float, position</code>.</p>

<p>Vi ska även lära oss att skapa separata CSS-mallar, så att vi kan separerar struktur (HTML) från formgivning (CSS). CSS-koden ska i denna uppgift alltså inte blandas med HTML-koden, utan finnas i ett eget <b>CSS-dokument</b>.</p>

