<h2>Modul 4 - CSS</h2>
<p>Hittills i kursen har vi helt fokuserat på HTML, alltså hur vi strukurerar upp information på en webbsida. Vi har tittat på bl.a. grundläggande struktur av text, bilder, tabeller, formulär. I denna modul ska vi börja titta på hur vi kan formge innehållet på våra webbsidor. Detta genom språket <b>CSS</b> (Cascading Style Sheet).</p>

<p>CSS används alltså för att formge webbsidor genom t.ex. berika dem med färg, layout och typografi. Vi kommer här i den första CSS-modulen att fokusera på de grundläggande sakerna för CSS, alltså hur man gör för att formge element på en webbplats. Hur kan jag sedan veta vilka alternativ för formgivning som jag har?</p>

<p>Man kan i CSS formge element på tre olika sätt:</p>
<ul>
    <li>Genom attributet "style" för ett HTML-element (<i>inline styling</i>)</li>
    <li>Genom formatmall i HTML-dokumentets &lt;head&gt;-element</li>
    <li>Genom extern formatmall (egen CSS-fil)</li>
</ul>
<p>I denna modul kommer vi att använda oss utav <b>inline styling</b>, och ni nästkommande moduler kommer vi att titta på de två andra sätten som vi kan formge våra webbsidor på.</p>

