<p>Kursen börjar med att ni ska bekanta er med <a href="mah.se/lms">It's Learning</a> (vår läroplattform) och dvwebb. Dvwebb är Malmö Högskolas webbpubliceringssystem som kommer att användas för denna kurs. Det fungerar i praktiken som ett webbhotell där ni kan ladda upp era filer för att kunna nå dem globalt (inte bara från er egna dator). Alla inlämningsuppgifter kräver att ni publicerar er lösning på dvwebb och bifogar länken till er lösning vid inlämning av uppgifter via It's Learning.</p>

