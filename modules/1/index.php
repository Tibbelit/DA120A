<h3>2. Kom igång med HTML</h3>
<p>När ni bekantat er med dvwebb och lyckats att ladda upp <i>och</i> surfa till en exempelwebbplats, då är det dags att börja lära sig skriva HTML-kod. HTML är ett märkspråk som berättar för oss <i>och</i> datorn vad för slags information ett text-dokument innehåller. Om du tittar runt på sidan du just nu besöker kan du säkert identifiera några olika delar, t.ex.</p>
<ul>
        <li>En meny</li>
        <li>En bild</li>
        <li>En huvudrubrik</li>
        <li>Underrubriker</li>
        <li>Paragrafer (brödtext)</li>
        <li>Punkt-listor</li>
        <li>... och flera andra sorters information</li>
</ul>
<p>Dessa olika delarna presenteras på olika sätt, trots att det egentligen bara är HTML-kod (text). I HTML-koden säger man nämnligen <i>vad</i> på sidan är en meny? <i>vad</i> på sidan är en bild? <i>vad</i> på sidan är en rubrik? <i>vad</i> på sidan är en punktlista? etc. Dessa kallas för <i>element</i>, som tillsammans bygger upp en webbsida.</p>

