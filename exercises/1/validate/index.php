<h2>Övning - Att validera HTML-kod</h2>
<p>Det är viktigt att de webbsidor som man skriver validerar - alltså följer de standarder som finns. Vi vill ju att alla följer samma regler, bland annat för att:</p>
<ul>
    <li>Det skapar kvalité på koden</li>
    <li>Webbsidan kommer visas på samma sätt i olika webbläsare</li>
    <li>Om man samarbetar kring koden är det viktigt att alla har samma inställning till att skriva bra kod</li>
</ul>
<h3>1. Övningen</h3>
<p>I denna övning finns en ZIP-fil med 3st HTML-dokument som inte validerar, er uppgift är att rätta till de så att de validerar - en bra kunskap att ha med er till kommande inlämningsuppgifter.</p>
<p>Ni validerar koden här: <a href="http://validator.w3.org/">http://validator.w3.org/</a></p>
